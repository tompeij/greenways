//This function implements the "right hand rule" required for leafletJS
//to render coordinate points for a polyline

function makePolyCCW(points) {
    for (var i = 0, len = points.length; i < len; i++) {
        let temp = points[i][0];
        points[i][0] = points[i][1];
        points[i][1] = temp;
    }
}

